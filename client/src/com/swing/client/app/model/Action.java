package com.swing.client.app.model;

/**
 * The enum Action.
 */
public enum Action {
  /**
   * Start server action.
   */
  START_SERVER,
  /**
   * Update client list action.
   */
  UPDATE_CLIENT_LIST,
  /**
   * Update record list action.
   */
  UPDATE_RECORD_LIST,
  /**
   * Client connected action.
   */
  CLIENT_CONNECTED,
  /**
   * Client disconnected action.
   */
  CLIENT_DISCONNECTED,

  /**
   * Update item action.
   */
  UPDATE_ITEM,

  /**
   * Delete item action.
   */
  DELETE_ITEM,

  /**
   * Create item action.
   */
  CREATE_ITEM,

  /**
   * Overflow item action.
   */
  OVERFLOW_ITEM,

  /**
   * Log history file action.
   */
  LOG_HISTORY_FILE,

  /**
   * Load history file action.
   */
  LOAD_HISTORY_FILE,

  /**
   * Search history action.
   */
  SEARCH_HISTORY,

}
